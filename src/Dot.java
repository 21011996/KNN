public class Dot {
    double x;
    double y;
    double z;
    int type;

    Dot(double x, double y, int type) {
        this.x = x;
        this.y = y;
        this.type = type;
    }

    Dot(double x, double y, double x, int type) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.type = type;
    }
}
